"""
Всі можливі комбінації чисел з 0 та 1
"""

n = int(input())  # кількість цифр

l = 1 << n - 1    # найменше n-значне число з нулів та одиниць
r = 1 << n        # найменше (n+1)-значне чило з нулів та одиниць

# послідовність чисел з проміжку [l, r) у двійковій системі числення
# буде будувати всі можливі комбінації n-значних чисел, що складаються з 0 та 1.
for i in range(l, r):
    print(bin(i))

